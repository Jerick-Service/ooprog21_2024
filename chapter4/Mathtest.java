class MathTest {
    public static void main(String[] args) {
     System.out.println("SQRT:  "+ Math.sqrt(90));
     System.out.println("SIN:  "+ Math.sin(90));
     System.out.println("COS:  "+ Math.cos(90));
     System.out.println("FLOOR:  "+ Math.floor(90.5));
     System.out.println("CEIL:  "+ Math.ceil(90.5));
     System.out.println("ROUND:  "+ Math.round(90.5));
     System.out.println("MAX:  "+ Math.max(90,67));
     System.out.println("MIN:  "+ Math.min(90,67));
     System.out.println("RANDOM:  "+ Math.random());
     
    }
}
